void aitown_db_free (
        aitown_db_t*db, const void ** chunk)
{
    DBG_ASSERT (chunk != NULL);
    DBG_ASSERT (db != NULL);
    DBG_ASSERT (db->driver != NULL);
    DBG_ASSERT (db->driver->free_chunk != NULL);

    if (*chunk == NULL) {
        return;
    }

    if (FUNC_OK == db->driver->free_chunk (db, (void*)*chunk)) {
        *chunk = NULL;
    }

}

