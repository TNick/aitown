void aitown_db_close (aitown_db_t** db)
{
    DBG_ASSERT (db != NULL);
    aitown_db_t *database = *db;

    if (database != NULL) {
        DBG_ASSERT (database->driver != NULL);
        DBG_ASSERT (database->driver->close != NULL);
        if (FUNC_OK == database->driver->close (database)) {
            *db = NULL;
        }
    }
}

