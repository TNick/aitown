TEST(plugin_testing,t1) {

	plugin_manager_t * mng;
	func_error_t err_code;
	
	err_code = plugin_manager_init (&mng);
	EXPECT_EQ(err_code, FUNC_OK);
	EXPECT_TRUE(mng != NULL);
	
	
	
	err_code = plugin_manager_end (&mng);
	EXPECT_EQ(err_code, FUNC_OK);
	EXPECT_TRUE(mng == NULL);
}
